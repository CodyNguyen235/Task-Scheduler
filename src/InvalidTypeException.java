
/**
 * The Class InvalidTypeException.
 * 
 * This is used to cause an exception when the type of the task is invalid
 */
public class InvalidTypeException extends Exception
{
	
	/**
	 * Instantiates a new invalid type exception.
	 *
	 * @param s the s
	 */
	InvalidTypeException(String s)
	{
		super(s);
	}
}
