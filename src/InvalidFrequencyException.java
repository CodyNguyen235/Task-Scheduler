
/**
 * The Class InvalidFrequencyException.
 * 
 * This is used to cause an exception when the frequency is not the correct number
 */
public class InvalidFrequencyException extends Exception
{
	
	/**
	 * Instantiates a new invalid frequency exception.
	 *
	 * @param s the s
	 */
	InvalidFrequencyException(String s)
	{
		super(s);
	}
}
